class OPVRandomizer {
public:
    static void randomize(uint8_t* data, size_t len) {
        for (size_t i = 0; i < len; ++i) {
            data[i] ^= mobilinkd::CCSDS_LFSR_TABLE[i % 255];
        }
    }

    static void derandomize(uint8_t* data, size_t len) {
        randomize(data, len);
    }
};

